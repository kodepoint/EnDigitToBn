public string cnDgt(string input)
        {
            return input.Replace('0', '০')
                .Replace('1', '১')
                .Replace('2', '২')
                .Replace('3', '৩')
                .Replace('4', '৪')
                .Replace('5', '৫')
                .Replace('6', '৬')
                .Replace('7', '৭')
                .Replace('8', '৮')
                .Replace('9', '৯');
        }
